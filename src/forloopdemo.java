public class forloopdemo {
    public static void main(String[] args) {

        int a=30;
        do {
            System.out.print(a);
            a++;
        }
        while (a<=70);
        System.out.println("-----------------------");
        System.out.println();
        int b=99;
        do {
            System.out.print(b);
            b--;
        }
        while (b>=1);




    }
}
